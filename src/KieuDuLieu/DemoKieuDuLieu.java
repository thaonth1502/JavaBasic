package KieuDuLieu;

public class DemoKieuDuLieu {
    public static void main(String[] args) {
        String name = "Thảo";
        char letter = 'a';

        int number = 10;
        float price = 2.5F; //lưu số tiền đô, lãi suất

        double income = 1000000000.1234568976555; //tiền, tỉ lệ trong máy móc

        boolean check = true; //Kiem tra dieu kien, dung nhieu trong testcase

        long soNguyen = 1000797L;

        System.out.println(name);
        System.out.println(letter);
        System.out.println(number);
        System.out.println(price);
        System.out.println(income);
        System.out.println(soNguyen);
        System.out.println(check);

        var age = 10; //var tự hiểu kiểu giá trị m mình gán, hạn chế dùng
        System.out.println(age);

    }
}
