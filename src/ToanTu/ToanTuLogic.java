package ToanTu;

public class ToanTuLogic {

    public static void main(String[] args) {

        int a = 10;
        int b = 20;
        int c = 15;

        //Dieu kien Va dung dau &&
        System.out.println((a > b) && (b > c));
        System.out.println((a < b) && (b > c));

        System.out.println((a > b) || (b > c));

    }
}
